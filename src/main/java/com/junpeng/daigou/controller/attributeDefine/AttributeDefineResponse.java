package com.junpeng.daigou.controller.attributeDefine;

public class AttributeDefineResponse {

	private String attributeDefineId;
	private String attributeName;
	private String categoryId;
	private String categoryName;
	public String getAttributeDefineId() {
		return attributeDefineId;
	}
	public void setAttributeDefineId(String attributeDefineId) {
		this.attributeDefineId = attributeDefineId;
	}
	public String getAttributeName() {
		return attributeName;
	}
	public void setAttributeName(String attributeName) {
		this.attributeName = attributeName;
	}
	
	public String getCategoryId() {
		return categoryId;
	}
	public void setCategoryId(String categoryId) {
		this.categoryId = categoryId;
	}
	public String getCategoryName() {
		return categoryName;
	}
	public void setCategoryName(String categoryName) {
		this.categoryName = categoryName;
	}


}
